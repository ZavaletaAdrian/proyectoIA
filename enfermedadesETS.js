function comparacion1(datosUsuario){
    var comparativaFinal = []
    sintEnf.forEach(sintoma => {
        var interseccion = [];
        for (let i = 0; i < sintoma.length; i++) {
            interseccion.push(Math.min(sintoma[i], datosUsuario[i]))
        }
        comparativaFinal.push(interseccion);
    })
    var resultadosFinales = [];
    var contador = 0;
    comparativaFinal.forEach(enfComp =>{
        contador++;
        var suma = 0;
        enfComp.forEach(sintoma =>{
            suma = suma + sintoma;
        })
        resultadosFinales.push([suma, contador])
    })
    
    resultadosFinales.sort().reverse();
    // MOSTRAR TOP 5
    
    var top5 = [];
    for (let i = 0; i < 5; i++) {
        top5.push(resultadosFinales[i])
    }
    // console.log(top5);
    presentarDatos1(top5);
}

