function seleccionarEnfermedades(listaEnfSel, sintomasUsuario){
    // console.log(listaEnfSel); //ENFEREMDADES QUE SE ELIGEN
    // console.log(sintomasUsuario);
    // console.log(Enfermedades);
    var sintomasEnfermedadesSeleccionadas = [];
    listaEnfSel.forEach(enfermedadSeleccionada => {
        sintomasEnfermedadesSeleccionadas.push(Object.values(Enfermedades)[enfermedadSeleccionada].sintomas)
    });
    // console.log(sintomasEnfermedadesSeleccionadas);
    var comparativaFinal = [];
    sintomasEnfermedadesSeleccionadas.forEach(sintoma=>{
        var interseccion = [];
        for (let i = 0; i < Object.keys(sintoma).length; i++) {
            interseccion.push(Math.min(Object.values(sintoma)[i], sintomasUsuario[i]))
        }
        comparativaFinal.push(interseccion);
    });
    // console.log(comparativaFinal);
    var resultadosFinales = [];
    var contador = 0;
    for (let i = 0; i < comparativaFinal.length; i++) {
        var sumaEnfermedad = 0;
        comparativaFinal[i].forEach(sintoma =>{
            sumaEnfermedad = sumaEnfermedad + sintoma;
        })
        resultadosFinales.push([sumaEnfermedad, listaEnfSel[i]])
    }
    
    resultadosFinales.sort().reverse();
    // console.log(resultadosFinales)
    presentarDatos2(resultadosFinales);
}

